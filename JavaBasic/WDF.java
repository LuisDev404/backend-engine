package JavaBasic;

public class WDF {

    public static void main(String[] args) {
        
        byte count = 0;
        while (count < 10) {
            count++;
            System.out.println(count);
        }

        System.out.println("[-----------------------------------------]");

        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
    }
}