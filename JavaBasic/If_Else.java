package JavaBasic;

public class If_Else {

    public static void main(String[] args) {

        int num = 10;

        if (num > 20) {
            System.out.println("É maior");
        } else if (num == 15) {
            System.out.println("É igual");
        } else if (num == 10) {
            System.out.println("É 10");
        } else {
            System.out.println("Outro valor");
        }

    }
}
