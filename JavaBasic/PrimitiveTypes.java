package JavaBasic;

public class PrimitiveTypes {
    public static void main(String[] args) {
        
        // Os tipos primitivos são:

        // byte: e um tipo que tem uma quantidade de armazenamento muito pequeno
        byte years = 127;
        System.out.println(years);

        // short: e um tipo que tem uma quantidade de armazenamento muito maior que o byte
        short money = 30000;
        System.out.println(money);

        // int: e um tipo que armazena números inteiros 
        int age = 10;
        System.out.println(age);

        // long: e tipo que não é muito diferente do int a sua única diferença e a quantidade de armazenamento
        long atoms = 10000000;
        System.out.println(atoms);

        // float: e um tipo que armazena números fracionados
        float percentage = 9;
        System.out.println(percentage);

        // double: e um tipo que pode armazenar números fracionados também sendo mais utilizado que o float por isso
        double promotion = 22032515;
        System.out.println(promotion);

        // boolean: e um tipo diferente dos outros ele so armazena true ou false e nada além disso
        boolean verdadeiro = true;
        boolean falso = false;
        System.out.println(verdadeiro); 
        System.out.println(falso); 

        // char: e um tipo que também tem um peculiariedade ele pode armazena tanto números mas eles são
        // traduzindos de acordo com a tabela ASCII quanto caracter mas somente 1
        char sexo = 'M';
        System.out.println(sexo);
    }
}
